  CODE=$(curl $CURL_K -s -o /dev/null -w '%{http_code}' "${AGENT_BASE}${path}" 2>&1)
  if [[ "$CODE" == "404" || "$CODE" == "500" ]]; then
    ALL_OK=false
    fail "T11.2 ${path}" "HTTP $CODE"
  fi
