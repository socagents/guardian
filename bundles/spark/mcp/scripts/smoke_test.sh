# T9.2 — manual trigger
# URL-encode FIRST_JOB before splicing it into the path: operators can
# create jobs via /jobs/new with names containing spaces or unicode
# (the backend's only constraint is "no slashes / dots" for filesystem
# safety — see CroniterJobScheduler._job_yaml_path). With raw splicing
# a name like "Say hi back" hits the route as `Say hi back/run`, which
# curl mangles and the FastMCP matcher can't reverse — both T9.2 and
# T9.3 then read empty status from a 4xx body.
#
# v0.3.23: prefer a MANIFEST-declared job over operator-created runtime
# jobs. Runtime jobs (created via the chat agent or operator-edited
# YAML) often depend on the Vertex provider via chat-style action
# types — if Vertex is degraded for any reason (KEK mismatch, etc.),
# those jobs return status=failed and break the smoke. Manifest jobs
# are bundle-declared and stable across deployments; they're the
# deterministic choice for a smoke probe.
FIRST_JOB=$(echo "${JOBS#*:}" | python3 -c "
import sys, json
d = json.load(sys.stdin)
jobs = d.get('jobs', [])
# Prefer manifest-source jobs (deterministic, stable). Fall back to
# the first job overall if no manifest jobs exist (pre-bundle test
# harness, theoretical edge case).
manifest = [j for j in jobs if j.get('source') == 'manifest']
pick = manifest[0] if manifest else (jobs[0] if jobs else None)
print(pick['name'] if pick else '')
" 2>/dev/null)
if [[ -n "$FIRST_JOB" ]]; then
  FIRST_JOB_ENC=$(python3 -c "import sys, urllib.parse as u; print(u.quote(sys.argv[1], safe=''))" "$FIRST_JOB")
  RUN_RESP=$(mcp_curl POST "/api/v1/jobs/${FIRST_JOB_ENC}/run" "")
  RUN_STATUS=$(echo "${RUN_RESP#*:}" | python3 -c "
import sys, json
d = json.load(sys.stdin)
print(d.get('run', {}).get('status', ''))
" 2>/dev/null)
  if [[ "$RUN_STATUS" == "success" || "$RUN_STATUS" == "skipped" ]]; then
    pass "T9.2 manual trigger of $FIRST_JOB returned status=$RUN_STATUS"
  else
    # v0.3.23: include the picked job name in the failure message so
    # future debugging starts with "what job did the smoke pick?" —
    # the previous failure message just showed status=failed which
    # offered zero signal about which job dispatched.
    fail "T9.2 manual trigger of '$FIRST_JOB'" "status=$RUN_STATUS"
  fi

  # T9.3 — disable + re-enable round trip
  DIS=$(mcp_curl POST "/api/v1/jobs/${FIRST_JOB_ENC}/disable" "")
  DIS_ENABLED=$(echo "${DIS#*:}" | python3 -c "import sys,json; print(json.load(sys.stdin)['job']['enabled'])" 2>/dev/null)
  EN=$(mcp_curl POST "/api/v1/jobs/${FIRST_JOB_ENC}/enable" "")
  EN_ENABLED=$(echo "${EN#*:}" | python3 -c "import sys,json; print(json.load(sys.stdin)['job']['enabled'])" 2>/dev/null)
  if [[ "$DIS_ENABLED" == "False" && "$EN_ENABLED" == "True" ]]; then
    pass "T9.3 disable→enable round-trip ($DIS_ENABLED → $EN_ENABLED)"
  else
    fail "T9.3 disable/enable" "disable=$DIS_ENABLED, enable=$EN_ENABLED"
  fi
fi

