    fail "smoke-test session cleanup" "HTTP $CLEANUP_CODE"
  fi
fi

CLEANUP_MEM=$(mcp_curl DELETE "/api/v1/memories/by-key/smoke-test-host?scope=agent")
CLEANUP_MEM_CODE="${CLEANUP_MEM%%:*}"
if [[ "$CLEANUP_MEM_CODE" == "200" ]]; then
  pass "removed smoke-test memory"
elif [[ "$CLEANUP_MEM_CODE" == "404" ]]; then
  skip "smoke-test memory cleanup" "already removed"
else
  fail "smoke-test memory cleanup" "HTTP $CLEANUP_MEM_CODE"
fi

# ────────────────────────────────────────────────────────────────
# Final report
# ────────────────────────────────────────────────────────────────

TOTAL=$((PASSED + FAILED + SKIPPED))
printf "\n${C_BOLD}━━━ Summary ━━━${C_RESET}\n"
printf "  Total checks : %d\n" "$TOTAL"
printf "  ${C_PASS}Passed       : %d${C_RESET}\n" "$PASSED"
printf "  ${C_WARN}Skipped      : %d${C_RESET}\n" "$SKIPPED"
printf "  ${C_FAIL}Failed       : %d${C_RESET}\n" "$FAILED"

if [[ "$FAILED" -gt 0 ]]; then
  printf "\n${C_FAIL}${C_BOLD}Failures:${C_RESET}\n"
  for f in "${FAILURES[@]}"; do
    printf "  • %s\n" "$f"
  done
  exit 1
fi

exit 0
