#!/usr/bin/env bash
# Phantom — full Phase 5-11a smoke test.
#
# Walks every capability introduced in the v1.2-conformance refactor and
# reports PASS/FAIL for each check. Designed to run ON THE PHANTOM VM
# HOST. It hits the embedded MCP at localhost:8080 and the Next.js
# agent UI at localhost:3000 (both host ports docker-compose maps).
# In-container filesystem checks reach into phantom_agent via
# `docker compose exec` — that container ships both the Next.js UI
# and the embedded MCP per the v1.2 bundle design (no separate
# phantom-mcp container).
#
# Usage (on the phantom VM):
#   cd $VM_REMOTE_REPO
#   MCP_TOKEN=<token> ./bundles/spark/mcp/scripts/smoke_test.sh
#
# Optional env:
#   MCP_BASE="https://localhost:8080"  (default; flip to http:// for non-TLS)
#   AGENT_BASE="https://localhost:3000" (default; flip to http:// for non-TLS)
#   COMPOSE_DIR=/path/to/repo           (default: \$PWD; must contain docker-compose.yml)
#   VERBOSE=1                           print every response payload
#
# Exit code: 0 on all-pass, 1 if any check failed, 2 on bad invocation.
#
# TLS: as of v1.2 stage-3F, the stack is HTTPS-by-default with a self-
# signed cert auto-generated by the agent's entrypoint at first boot.
# This script's curl calls all pass -k (skip cert verify) so they accept
# the self-signed material. If you've configured a CA-signed cert and
# want strict verification, drop -k via the CURL_K env override.

set -uo pipefail

MCP_BASE="${MCP_BASE:-https://localhost:8080}"
AGENT_BASE="${AGENT_BASE:-https://localhost:3000}"
MCP_TOKEN="${MCP_TOKEN:-}"
VERBOSE="${VERBOSE:-0}"
COMPOSE_DIR="${COMPOSE_DIR:-$PWD}"
# -k accepts self-signed certs (the default for TLS-by-default mode).
# Override with CURL_K="" if you've installed a real CA cert and want
# strict verification.
CURL_K="${CURL_K:--k}"

if [[ -z "$MCP_TOKEN" ]]; then
  echo "ERROR: MCP_TOKEN not set in env."
  exit 2
fi

if ! command -v curl >/dev/null 2>&1; then
  echo "ERROR: curl not found. This script must run on the VM host, not"
  echo "       inside a container."
  exit 2
fi

# Filesystem checks reach into phantom_agent (which now hosts the
# embedded MCP — no separate phantom-mcp container exists).
DOCKER_EXEC=""
if command -v docker >/dev/null 2>&1 && [[ -f "$COMPOSE_DIR/docker-compose.yml" ]]; then
  DOCKER_EXEC="docker compose -f $COMPOSE_DIR/docker-compose.yml exec -T phantom-agent"
fi

PASSED=0
FAILED=0
SKIPPED=0
declare -a FAILURES=()

# ─── Output helpers ──────────────────────────────────────────────

C_PASS='\033[0;32m'
C_FAIL='\033[0;31m'
C_WARN='\033[0;33m'
C_BOLD='\033[1m'
C_DIM='\033[2m'
C_RESET='\033[0m'

section() {
  printf "\n${C_BOLD}━━━ %s ━━━${C_RESET}\n" "$1"
}

pass() {
  PASSED=$((PASSED + 1))
  printf "  ${C_PASS}✓${C_RESET} %s\n" "$1"
}

fail() {
  FAILED=$((FAILED + 1))
  FAILURES+=("$1")
  printf "  ${C_FAIL}✗${C_RESET} %s\n" "$1"
  if [[ -n "${2:-}" ]]; then
    printf "    ${C_DIM}%s${C_RESET}\n" "$2"
  fi
}

skip() {
  SKIPPED=$((SKIPPED + 1))
  printf "  ${C_WARN}~${C_RESET} %s ${C_DIM}(skipped: %s)${C_RESET}\n" "$1" "${2:-no reason}"
}

verbose() {
  if [[ "$VERBOSE" == "1" ]]; then
    printf "    ${C_DIM}%s${C_RESET}\n" "$1"
  fi
}

# ─── HTTP helpers ────────────────────────────────────────────────

# mcp_curl <method> <path> [body]
# Returns "STATUS:BODY" with status code prefixed.
mcp_curl() {
  local method="$1"
  local path="$2"
  local body="${3:-}"
  local args=(-s -w '\nHTTPSTATUS:%{http_code}' -X "$method" -H "Authorization: Bearer $MCP_TOKEN")
  if [[ -n "$CURL_K" ]]; then
    args+=("$CURL_K")
  fi
  if [[ -n "$body" ]]; then
    args+=(-H "Content-Type: application/json" -d "$body")
  fi
  local response
  response=$(curl "${args[@]}" "${MCP_BASE}${path}" 2>&1)
  local http_status
  http_status=$(echo "$response" | tail -n1 | sed 's/HTTPSTATUS://')
  local response_body
  response_body=$(echo "$response" | sed -e '$d')
  echo "${http_status}:${response_body}"
}

agent_curl() {
  local path="$1"
  local response
  response=$(curl $CURL_K -s -w '\nHTTPSTATUS:%{http_code}' "${AGENT_BASE}${path}" 2>&1)
  local http_status
  http_status=$(echo "$response" | tail -n1 | sed 's/HTTPSTATUS://')
  local response_body
  response_body=$(echo "$response" | sed -e '$d')
  echo "${http_status}:${response_body}"
}

# expect_json_field <STATUS:BODY> <jq-filter> <expected-value>
# Returns 0 if the JSON body's filter result equals expected.
expect_json_field() {
  local response="$1"
  local filter="$2"
  local expected="$3"
  local body="${response#*:}"
  local actual
  actual=$(echo "$body" | python3 -c "import sys, json; d = json.load(sys.stdin); $filter" 2>/dev/null)
  if [[ "$actual" == "$expected" ]]; then
    return 0
  else
    echo "   expected: $expected"
    echo "   actual:   $actual"
    return 1
  fi
}

