def _yaml_quote(s: str) -> str:
    """Single-quote a string for YAML, doubling internal single quotes per spec."""
    return "'" + s.replace("'", "''") + "'"


def _yaml_scalar(v: str) -> str:
    """Render a string scalar with safe quoting.

    YAML will mis-parse:
      - bare strings starting with @ * ! & % > | (reserved leading chars)
      - things that LOOK like dates ('2024-01-15'), times, ints, floats, bools
      - strings with leading/trailing whitespace
      - strings containing : # or starting with - (list-marker confusion)

    Safest default: single-quote anything that isn't a plain alphanum/word.
    """
    if v == "":
        return "''"
    # Anything not strictly word-chars + a few safe specials → quote
    safe = all(c.isalnum() or c in "_./() " for c in v)
    if not safe:
        return _yaml_quote(v)
    # Quote if it could parse as int/float/bool/date
    if v.lower() in ("true", "false", "yes", "no", "null", "~"):
        return _yaml_quote(v)
    try:
        int(v)
        return _yaml_quote(v)
    except ValueError:
        pass
    try:
        float(v)
        return _yaml_quote(v)
    except ValueError:
        pass
    # Date-looking? (yyyy-mm-dd)
    if len(v) >= 8 and v[:4].isdigit() and v[4] == "-":
        return _yaml_quote(v)
    return v


def render_field_yaml(field: dict[str, Any], indent: str = "  ") -> str:
    """Render a single field dict as YAML — preserves key order from the dict
    so the output is deterministic for diff review."""
    name_rendered = _yaml_scalar(field["name"])
    lines = [f"{indent}- name: {name_rendered}"]
    body_indent = indent + "  "
    keys_in_order = ("type", "is_array", "is_meta", "description",
                     "example", "enum_values", "regex_pattern",
                     "observable_override")
    for k in keys_in_order:
        if k not in field:
            continue
        v = field[k]
        if isinstance(v, bool):
            lines.append(f"{body_indent}{k}: {str(v).lower()}")
        elif isinstance(v, str):
            lines.append(f"{body_indent}{k}: {_yaml_scalar(v)}")
        elif isinstance(v, list):
            lines.append(f"{body_indent}{k}:")
            for item in v:
                if isinstance(item, str):
                    lines.append(f"{body_indent}- {_yaml_scalar(item)}")
                else:
                    lines.append(f"{body_indent}- {item}")
        else:
            lines.append(f"{body_indent}{k}: {v}")
    return "\n".join(lines)


def update_one_yaml(yaml_path: Path, fields: list[dict[str, Any]]) -> tuple[bool, str]:
    """Rewrite `fields: []` block (or `fields:\\n  - ...` block) with the
    new list. Returns (ok, message). Idempotent — running twice produces
    the same file."""
    if not yaml_path.exists():
        return False, f"missing: {yaml_path}"

    content = yaml_path.read_text()
    # Locate the `fields:` line.
    lines = content.split("\n")
    # Find the fields block — start at `fields:` end at next top-level key
    start = None
    end = None
    for i, line in enumerate(lines):
        if line.startswith("fields:") or line.startswith("fields :"):
            start = i
            # End is the next top-level key (line not starting with whitespace
            # or comment) OR end of file.
            for j in range(i + 1, len(lines)):
                if not lines[j].startswith((" ", "\t", "#", "-")) and lines[j].strip():
                    end = j
                    break
            if end is None:
                end = len(lines)
            break

    if start is None:
        return False, f"no 'fields:' key in {yaml_path}"

    # Build replacement
    new_block = ["fields:"]
    for f in fields:
        new_block.append(render_field_yaml(f))
    new_content = "\n".join(lines[:start] + new_block + lines[end:])
    if not new_content.endswith("\n"):
        new_content += "\n"

    yaml_path.write_text(new_content)
    return True, f"updated {yaml_path.name}: {len(fields)} fields"


def main() -> int:
    if not BUNDLE_ROOT.exists():
        print(f"ERROR: bundle root not found: {BUNDLE_ROOT}", file=sys.stderr)
        return 1

    print(f"=== v0.16.0 field extension ({len(DATASET_FIELDS)} datasets) ===\n")
    ok = 0
    failed: list[str] = []
    for dataset_id, fields in DATASET_FIELDS.items():
        yaml_path = BUNDLE_ROOT / dataset_id / "data_source.yaml"
        success, msg = update_one_yaml(yaml_path, fields)
        status = "✓" if success else "✗"
        print(f"  {status} {msg}")
        if success:
            ok += 1
        else:
            failed.append(dataset_id)

    print(f"\n=== {ok}/{len(DATASET_FIELDS)} datasets updated ===")
    if failed:
        print("\nFailed:")
        for f in failed:
            print(f"  - {f}")
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
