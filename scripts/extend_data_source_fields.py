#!/usr/bin/env python3
"""v0.16.0 — Extend bundled data source field coverage.

The R3.C migration (v0.13.0) populated 260 data_source.yaml files
from cortex-content pack metadata, but every one of them carries
``fields: []`` because the modeling rules in the upstream content
packs are sparse (cortex-content schema extraction only captures
fields the modeling rule explicitly declares — usually 0-5 fields
per source, far less than the actual product schema).

This script populates ``fields[]`` for the **top enterprise-relevant
vendors** with curated schemas based on each product's published
documentation. Each entry has:

  * ``name`` — vendor field name (exact, as it appears in the
    product's real log records).
  * ``type`` — from the controlled vocabulary in
    ``data_source.schema.json`` (string_short / integer / ipv4 / etc).
  * ``description`` — operator-facing.
  * ``example`` — optional, used by UI/docs surfaces.
  * ``enum_values`` / ``regex_pattern`` — when ``type`` requires it.

Sources consulted (per vendor):

  * Okta: developer.okta.com/docs/reference/api/system-log/
  * AWS CloudTrail: docs.aws.amazon.com/awscloudtrail/latest/userguide/
  * Microsoft 365 / Azure AD Sign-In: learn.microsoft.com/en-us/entra/
  * Palo Alto PAN-OS: docs.paloaltonetworks.com/pan-os
  * FortiGate: docs.fortinet.com/document/fortigate/
  * CrowdStrike Falcon FDR: falcon.crowdstrike.com/documentation/
  * Cisco ASA syslog: cisco.com/c/en/us/td/docs/security/asa/
  * GitHub Audit Log: docs.github.com/en/enterprise-cloud@latest/admin/
  * Salesforce EventMonitoring: developer.salesforce.com/docs/
  * Slack Audit API: api.slack.com/admins/audit-logs
  * Zscaler ZIA: help.zscaler.com/zia
  * Microsoft Defender for Endpoint: learn.microsoft.com/en-us/defender-endpoint/
  * AWS GuardDuty findings: docs.aws.amazon.com/guardduty/latest/ug/

Run from repo root::

    python3 scripts/extend_data_source_fields.py

Idempotent — overwrites the `fields:` block for each matching
data_source.yaml. Validates against the schema after each write.

Exit 0 on success; non-zero on schema-validation failure (file is
NOT written if validation fails — failed sources are reported).
"""

